// Caracters ASCII fooooora!!
	function ave_ascii($cadena) {	
		for ($i = 0; $i < 30; $i++) {
			$cadena = str_replace(chr($i), '', $cadena, $cadena);
		}
		return $cadena;
	}

// Comes rares foora també..!	
	function ave_comes_rares($cadena) {
		$coma=array('a'=>'“','b'=>'”');
		$nova=array('a'=>'"','b'=>'"');
		
		return str_replace($coma,$nova,$cadena,$cadena);	
	}

